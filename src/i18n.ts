const storedLanguage =
  typeof window === 'undefined' ? null : window.localStorage.getItem('lab04-language')
const language = storedLanguage === 'en' || storedLanguage === 'es' ? storedLanguage : 'es'

i18n.use(initReactI18next).init({
  resources,
  lng: language,
  fallbackLng: 'es',
  supportedLngs: ['es', 'en'],
  interpolation: { escapeValue: false },
})

i18n.on('languageChanged', (nextLanguage) => {
  if (typeof window !== 'undefined') window.localStorage.setItem('lab04-language', nextLanguage)
  if (typeof document !== 'undefined') document.documentElement.lang = nextLanguage
})

export default i18n
